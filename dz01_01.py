"""
Вариант №14:
Для целого числа К от 1 до 9 напечатать фразу «мне К лет»,
учитывая при этом, что при некоторых значениях К слово «лет» надо заменить на слово «год» или «года».
"""
k = int(input("Ввведите К от 1 до 9: "))
if k == 1:
    print("Мне", k, "год")
elif 2 <= k <= 4:
    print("Мне", k, "года")
elif 5 <= k <= 9:
    print("Мне", k, "лет")
else:
    print("Вы ввели число не от 1 до 9")